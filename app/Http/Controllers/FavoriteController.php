        $good = New Good;
        $good->user_id = Auth::id();
        $good->post_id = $request->post_id;
        $good->save();

        return back();
    }
    
    public function remove(Request $request)
    {
       $good = Good::where('user_id', Auth::id())->where('post_id', $request->post_id)->first();
       $good->delete();
       
       return back();
    }

